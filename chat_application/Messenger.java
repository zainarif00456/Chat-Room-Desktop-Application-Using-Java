public String receiveDatabase(String ph)
    {
        phnum = ph;
        DatabaseConnection dbc = new DatabaseConnection();
        Connection con = dbc.database();
        String query = "select PhoneNo, FirstName, LastName, Gender, User_Image from UserInfo where PhoneNo = ?";
        try {
            PreparedStatement p = con.prepareStatement(query);
            p.setString(1, phnum);
            ResultSet r = p.executeQuery();
            if(r.next())
            {
                phnum = r.getString("PhoneNo");
                fname = r.getString("FirstName");
                lname = r.getString("LastName");
                picture = r.getBytes("User_Image");
                img = new ImageIcon(picture);
            }
            else{
                new SignUpPage().setVisible(true);
                }
        } catch (SQLException ex) {
           
        }
        
        return fname+" "+lname;
        
    }
