    public void getMessages(){
        DatabaseConnection db = new DatabaseConnection();
        Connection con = db.database();
        try{
            String query = "select count(UserID) as TotalMsgs from Text_Messages";
            PreparedStatement p = con.prepareStatement(query);
            ResultSet r = p.executeQuery();
            if(r.next())
            {
                totalmsg = r.getInt("TotalMsgs");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No Record Found...");
            }
            
            for(int i = 1; i<=totalmsg; i++){
                query = "select TxtMessage from Text_Messages where ID = ?";
                p = con.prepareStatement(query);
                p.setInt(1, i);
                r = p.executeQuery();
                if(r.next()){
                    String msg = r.getString("TxtMessage");
                    jdisplaymsg.append(msg+"\n");
                }
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "ERROR: "+e);
        }
    }
    
